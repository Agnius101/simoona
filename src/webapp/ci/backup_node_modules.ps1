param (
    [string]$projId ,
    [string]$WorkDir
)

$root = "c:\_buildCache"

if (!(Test-Path $root))
{
    mkdir $root
}

if (!(Test-Path $root\$projId))
{
    mkdir "$root\$projId"
}

if (Test-Path "$root\$projId\node_modules")
{
    Remove-Item "$root\$projId\node_modules"
}

Move-Item "$WorkDir\node_modules" "$root\$projId"


if (Test-Path "$root\$projId\bower_components")
{
    Remove-Item "$root\$projId\bower_components"
}
 
Move-Item "$WorkDir\bower_components" "$root\$projId"