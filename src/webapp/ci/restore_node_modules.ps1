param (
    [string]$projId ,
    [string]$WorkDir
)

$root = "c:\_buildCache"
 
if(Test-Path "$root\$projId\node_modules")
{
    Move-Item "$root\$projId\node_modules" "$WorkDir\node_modules"
}

if(Test-Path "$root\$projId\bower_components")
{
    Move-Item "$root\$projId\bower_components" "$WorkDir\bower_components"
}